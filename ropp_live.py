"""Feed the existing ROPP builder (UPDATE_DASHBOARD.py) from LIVE ServiceTitan data.

Reads the cached API report payloads in cache/*.json (fetched via the Reporting API),
converts each to the exact Excel row shape the builder expects (API field order already
matches the Excel column order — grouped reports just get a leading blank column + tech
group-header rows), monkeypatches U.load_rows, and regenerates the ROPP data blocks.

Run cache_reports() first (separate step) to refresh cache/*.json from the API.
"""
